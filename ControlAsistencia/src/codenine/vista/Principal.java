    void CargarComboBox() {

        AsistenciaInterfaz proc = new AsistenciaImpl();
        comboBox.setModel(proc.combo());
        //se agrega model al JTable
        tbAsistencia.setRowHeight(22);//altura de filas
        //se indica que columna tendra el JComboBox
        tbAsistencia.getColumnModel().getColumn(6).setCellEditor(new DefaultCellEditor(comboBox));
        tbAsistencia.setDefaultRenderer(Object.class, new CeldaRenderer(6));
    }

    void CargarComboBoxOficina() {
        OficinaInterfaz proc = new OficinaImpl();
        cmbOficina.setModel(proc.combo());
    }

